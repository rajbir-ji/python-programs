my_string="Hello, welcome to the world of Python programming."
# slicing
print("Sliced String (0-5):", my_string[0:5])
print("Sliced String (7-14):", my_string[7:14])
print("Sliced String (15-end):", my_string[15:])
print("Sliced String (start-5):", my_string[:5])
print("Sliced String (only 2nd char):", my_string[:2])
print("Sliced String (reversed):", my_string[:-1])
